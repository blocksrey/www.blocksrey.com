let rand = Math.random
let floor = Math.floor

let grouptot = 0
let igroup = []
let nbool = {}

for (let spec in specgroup) {
	let group = specgroup[spec]
	igroup[grouptot] = specgroup[spec]
	++grouptot
	for (i in specgroup[spec])
		nbool[specgroup[spec][i]] = 1
}

function randint(n) { return floor(n*rand()) }
function randarr(a) { return a[randint(a.length)] }
function randomselect() { return randarr(igroup[randint(grouptot)]) }

function randomselectiters(n) {
	if (nbool[n]) {
		let iter = 0
		for (;;) {
			++iter
			if (randomselect() === n)
				return iter
		}
	}
}