let rand  = Math.random;
let floor = Math.floor;

let grouptot  = 0;
let ind_group = [];
let name_bool = {};

for (let spec in spec_group) {
	let group = spec_group[spec]
	ind_group[grouptot] = spec_group[spec];
	++grouptot;
	for (ind in spec_group[spec]) {
		name_bool[spec_group[spec][ind]] = true;
	}
}

function randint(range) {
	return floor(range*rand());
}

function arrayrand(arr) {
	return arr[randint(arr.length)];
}

function randomselect() {
	return arrayrand(ind_group[randint(grouptot)]);
}

function randomselectiters(name) {
	if (name_bool[name]) {
		let iter = 0;
		while (true) {
			++iter;
			if (randomselect() == name) return iter;
		};	
	}
}